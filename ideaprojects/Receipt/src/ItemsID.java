public class ItemsID {
    int numberOfShirt, numberOfSkirt, numberOfScarf;

    public ItemsID(int numberOfShirt, int numberOfSkirt, int numberOfScarf) {
        this.numberOfShirt = numberOfShirt;
        this.numberOfSkirt = numberOfSkirt;
        this.numberOfScarf = numberOfScarf;
    }

    public int getNumberOfShirt() {
        return numberOfShirt;
    }

    public void setNumberOfShirt(int numberOfShirt) {
        this.numberOfShirt = numberOfShirt;
    }

    public int getNumberOfSkirt() {
        return numberOfSkirt;
    }

    public void setNumberOfSkirt(int numberOfSkirt) {
        this.numberOfSkirt = numberOfSkirt;
    }

    public int getNumberOfScarf() {
        return numberOfScarf;
    }

    public void setNumberOfScarf(int numberOfScarf) {
        this.numberOfScarf = numberOfScarf;
    }
}
