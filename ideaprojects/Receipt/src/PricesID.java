public class PricesID {
    double priceShirt, priceSkirt, priceScarf;

    public PricesID(double priceShirt, double priceSkirt, double priceScarf) {
        this.priceShirt = priceShirt;
        this.priceSkirt = priceSkirt;
        this.priceScarf = priceScarf;
    }

    public double getPriceShirt() {
        return priceShirt;
    }

    public void setPriceShirt(double priceShirt) {
        this.priceShirt = priceShirt;
    }

    public double getPriceSkirt() {
        return priceSkirt;
    }

    public void setPriceSkirt(double priceSkirt) {
        this.priceSkirt = priceSkirt;
    }

    public double getPriceScarf() {
        return priceScarf;
    }

    public void setPriceScarf(double priceScarf) {
        this.priceScarf = priceScarf;
    }
}
